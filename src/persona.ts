export function getTimeBasedGreeting(): string {
    const hour = new Date().getHours();
    if (hour < 6) {
        return '还不睡？你这是打算把自己写进鸣神大社的夜谈怪谈里吗？';
    }
    if (hour < 9) {
        return '早安，小家伙。趁脑子还清醒，把最难的那部分先处理掉。';
    }
    if (hour < 12) {
        return '上午正适合做些需要判断力的事。希望你别浪费这段时间。';
    }
    if (hour < 14) {
        return '中午了。去吃饭吧，空着肚子写出的代码通常也没什么灵气。';
    }
    if (hour < 18) {
        return '下午好。若你现在还有耐心，那就继续把剧情推下去。';
    }
    if (hour < 22) {
        return '晚上总适合写些更有趣的东西。只要别把自己写垮就行。';
    }
    return '夜深了。就算灵感还在，也别让身体先退稿。';
}
