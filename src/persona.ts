export function getFinale(tasksDone: number): string {
    const base = pick(FINALE_LINES);
    if (tasksDone > 0) {
        return `${base}\n今日你完成了 ${tasksDone} 个番茄钟。嗯，这份效率还算能看。`;
    }
    return base;
}

